
public class Arithmetic {
	public static void main(String[] args) {
		int a = 5;
		int b = 7;
        int sum = a + b;
		int sub = a - b;
		int mul = a * b;
		int div = a / b;
		int rem = a % b;

		System.out.println("a + b = " + sum);
		System.out.println("a - b = " + sub);
	    System.out.println("a * b = " + mul);
		System.out.println("a / b = " + div);
		System.out.println("a % b = " + rem);
	}
}
