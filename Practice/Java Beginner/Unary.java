
public class Unary {

	public static void main(String[] args) {
	int x = 100;
		int y = 22;
        int p1 = +x;
        int m1 = -y;
        int p2 = ++x;
        int m2 = --y;
        System.out.println("Unary plus " + p1);
		System.out.println("Unary minus " + m1);
		System.out.println("Increment " + p2);
		System.out.println("Decrement " + m2);
		}
}
